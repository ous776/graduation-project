     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $imageNames = [];
        if ($request->hasFile('media')) {
            $images = $request->file('media');
            foreach ($images as $image) {
                //$path = 'EventMedia-' . time() . '.' . $image->getClientOriginalExtension();
                //$imageNames[] = $image->storeAs('Events/Image', $path, 'public');
                $imageNames[] = Storage::disk('public')->put('Events/Image', $image);

            }
        }

        $event = Event::create([
            'title' => $request->title,
            'message_content' => $request->message,
            'user_id' => $request->user()->id,
            'image' => json_encode($imageNames),
        ]);

        return redirect(route('events.index'));
    }

